const boxQuiz = {
  id: 'g2-box',
  type: 'choice',
  difficulty: 1,
  make() {
    const q = pick([
      { p: 'はこの めん(かべ)は いくつ?', a: 6 },
      { p: 'さいころの ちょうてん(かど)は いくつ?', a: 8 },
      { p: 'はこの へんは いくつ?', a: 12 },
    ])
    return {
      prompt: `📦\n${q.p}`,
      speakText: q.p,
      answer: q.a,
      data: { choices: distractors(q.a, 3, 3, 1) },
      hints: ['はこの もけいを おもいうかべよう', 'めんは 6、へんは 12、ちょう点は 8 だよ'],
      explain: `こたえは ${q.a}こ だよ`,
    }
  },
}

