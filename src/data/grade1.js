const longerCompare = {
  id: 'g1-longer',
  type: 'choice',
  difficulty: 1,
  make() {
    const items = [
      { label: 'えんぴつ', emoji: '✏️', len: Math.random() },
      { label: 'クレヨン', emoji: '🖍️', len: Math.random() },
    ]
    const longer = items[0].len > items[1].len ? items[0] : items[1]
    const bars = items
      .map((it) => `${it.emoji} ${'─'.repeat(it === longer ? 8 : 4)}`)
      .join('\n')
    return {
      prompt: `${bars}\nながいのは どっち?`,
      speakText: 'ながいのは どっち?',
      answer: longer.label,
      data: { choices: [items[0].label, items[1].label] },
      hints: ['はしを そろえて くらべよう', 'せんが とおくまで のびている ほうが ながいよ'],
      explain: `${longer.label}のほうが ながいね`,
    }
  },
}

